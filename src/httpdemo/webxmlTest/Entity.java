package httpdemo.webxmlTest;

public class Entity {
    private String name;
    private String clazz;

    public Entity(){
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public String getClazz() {
        return clazz;
    }
    public void setClazz(String clazz) {
        this.clazz = clazz;
    }

}
